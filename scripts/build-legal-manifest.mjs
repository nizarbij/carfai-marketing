/**
 * scripts/build-legal-manifest.mjs
 * ─────────────────────────────────────────────────────────────────────────────
 * Inline content/legal/<locale>/<slug>.md as a typed static module so the
 * edge runtime (Cloudflare Workers / Pages) can render legal pages without
 * any fs/path access at request time.
 *
 * Why: @cloudflare/next-on-pages requires every Next route to opt into
 * `export const runtime = 'edge'`. The edge runtime has no Node built-ins,
 * so the original renderLegalPage() in app/[locale]/legal/_lib/render.tsx
 * couldn't readFile at request time. This script flattens the markdown
 * tree into a single TS module imported by the renderer.
 *
 * Output: app/[locale]/legal/_lib/manifest.generated.ts (gitignored).
 * Re-runs automatically before `next build` via the prebuild hook in
 * package.json.
 * ─────────────────────────────────────────────────────────────────────────────
 */

import { readFileSync, readdirSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT      = join(__dirname, '..');
const LEGAL_DIR = join(ROOT, 'content', 'legal');
const OUT_FILE  = join(ROOT, 'app', '[locale]', 'legal', '_lib', 'manifest.generated.ts');

const LOCALES = ['en', 'fr', 'es', 'ar'];

const content = {};
for (const locale of LOCALES) {
  content[locale] = {};
  const dir = join(LEGAL_DIR, locale);
  if (!existsSync(dir)) {
    console.warn(`[build-legal-manifest] missing locale dir: ${dir}`);
    continue;
  }
  for (const file of readdirSync(dir)) {
    if (!file.endsWith('.md')) continue;
    const slug = file.replace(/\.md$/, '');
    content[locale][slug] = readFileSync(join(dir, file), 'utf-8');
  }
}

const header = `// AUTOGENERATED by scripts/build-legal-manifest.mjs — do not edit by hand.
// Inlines content/legal/<locale>/<slug>.md at build time so the Cloudflare
// edge runtime can render legal pages without fs/path access.
//
// Regenerated automatically before \`next build\` via the prebuild hook.
// To regen manually: npm run build:legal

export type LegalSlug   = 'privacy' | 'terms' | 'eula' | 'ai-disclosure' | 'cookies' | 'dpa' | 'aup';
export type LegalLocale = 'en' | 'fr' | 'es' | 'ar';

export const LEGAL_CONTENT: Record<LegalLocale, Partial<Record<LegalSlug, string>>> = `;

writeFileSync(OUT_FILE, header + JSON.stringify(content, null, 2) + ';\n', 'utf-8');

const totalDocs = Object.values(content).reduce((n, m) => n + Object.keys(m).length, 0);
console.log(`[build-legal-manifest] wrote ${totalDocs} docs across ${LOCALES.length} locales → ${OUT_FILE}`);
